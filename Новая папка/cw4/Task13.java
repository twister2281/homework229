package Tasks_4;

public class Task13 {
    public static void main(String[] args) {
        int rows = 4; // Количество строк в массиве
        int[][] array = new int[rows][]; // Создаем массив с переменным количеством столбцов

        // Инициализация элементов массива
        array[0] = new int[] {1}; // Первая строка содержит один элемент
        array[1] = new int[] {2, 3}; // Вторая строка содержит два элемента
        array[2] = new int[] {4, 5, 6}; // Третья строка содержит три элемента
        array[3] = new int[] {7, 8, 9}; // Четвертая строка содержит три элемента

        // Вызов метода для поиска максимального элемента в массиве
        int[] maxElement = findMaxElement(array);
        System.out.println("Максимальный элемент: " + maxElement[0] + " на позиции (" + maxElement[1] + ", " + maxElement[2] + ")");
        // Выводим максимальный элемент и его позицию
    }

    // Метод для поиска максимального элемента в массиве
    public static int[] findMaxElement(int[][] array) {
        int max = Integer.MIN_VALUE; // Инициализируем максимальное значение минимально возможным
        int row = -1, col = -1; // Переменные для хранения позиции максимального элемента

        // Проходим по всем элементам массива
        for (int i = 0; i < array.length; i++) { // Перебор строк
            for (int j = 0; j < array[i].length; j++) { // Перебор столбцов
                if (array[i][j] > max) { // Если текущий элемент больше максимального
                    max = array[i][j]; // Обновляем максимальный элемент
                    row = i; // Запоминаем строку
                    col = j; // Запоминаем столбец
                }
            }
        }
        return new int[]{max, row, col}; // Возвращаем максимальный элемент и его позицию
    }
}
