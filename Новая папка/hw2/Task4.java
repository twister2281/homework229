import java.util.Scanner;

public class Task4 {
    public static void main(String[] args) {
        // Создаем объект Scanner для ввода данных с клавиатуры
        Scanner scanner = new Scanner(System.in);

        // Ввод размеров матрицы
        System.out.print("Введите количество строк (m): ");
        int m = scanner.nextInt();  // Вводим количество строк
        System.out.print("Введите количество столбцов (n): ");
        int n = scanner.nextInt();  // Вводим количество столбцов

        // Инициализируем матрицу размера m на n
        double[][] matrix = new double[m][n];

        // Заполнение матрицы значениями
        System.out.println("Введите элементы матрицы:");
        for (int i = 0; i < m; i++) {  // Проходим по строкам матрицы
            for (int j = 0; j < n; j++) {  // Проходим по столбцам каждой строки
                matrix[i][j] = scanner.nextDouble();  // Вводим элемент матрицы
            }
        }

        // Переменная для хранения суммы максимальных элементов в строках
        double sumMaxValues = 0;

        // Поиск максимальных элементов в каждой строке и их сумма
        for (int i = 0; i < m; i++) {  // Проходим по всем строкам
            double maxInRow = matrix[i][0];  // Инициализируем максимальное значение в строке первым элементом
            for (int j = 1; j < n; j++) {  // Проходим по остальным столбцам строки
                if (matrix[i][j] > maxInRow) {  // Если текущий элемент больше найденного максимума
                    maxInRow = matrix[i][j];  // Обновляем максимальное значение
                }
            }
            sumMaxValues += maxInRow;  // Добавляем найденное максимальное значение строки к общей сумме
        }

        // Выводим результат — сумму максимальных элементов в строках
        System.out.println("Сумма наибольших значений в строках: " + sumMaxValues);

        // Закрываем сканер, чтобы освободить ресурсы
        scanner.close();
    }
}
