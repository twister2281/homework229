import java.util.Scanner;

public class Task1 {
    public static void main(String[] args) {
        // Объявление константы для числа PI
        final double PI = 3.141592653589793;

        // Создаём объект Scanner для получения ввода пользователя
        Scanner scanner = new Scanner(System.in);

        // Запрашиваем основание системы счисления
        System.out.println("Введите основание системы счисления k (2 < k < 9):");
        int k = scanner.nextInt();

        // Проверяем, что k в допустимом диапазоне
        if (k <= 2 || k >= 9) {
            System.out.println("Ошибка: k должно быть в диапазоне 2 < k < 9.");
            return;
        }

        // Запрашиваем число в системе счисления с основанием k
        System.out.println("Введите число n в системе счисления с основанием k:");
        int n = scanner.nextInt();

        // Переменные для хранения результата и степени
        int decimal = 0;
        int power = 0;

        // Преобразуем число из системы счисления с основанием k в десятичную систему
        while (n > 0) {
            int digit = n % 10;  // Берём последнюю цифру
            if (digit >= k) {  // Проверка, что цифра меньше основания системы
                System.out.println("Ошибка: цифра числа n не может быть больше или равна основанию системы.");
                return;
            }
            decimal += digit * Math.pow(k, power);  // Прибавляем значение цифры в десятичной системе
            n /= 10;  // Убираем последнюю цифру числа
            power++;  // Переходим к следующей степени
        }

        // Выводим результат в десятичной системе
        System.out.println("Число в десятичной системе: " + decimal);
    }
}
